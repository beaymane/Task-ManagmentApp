package org.example.managnentapp.Dto;

import org.example.managnentapp.Enum.UserRole;

public class UserDTO {

    private Long id;
    private String username;
    private UserRole role;
    private Long departmentId; // Only include the ID to avoid circular references
    private String departmentName; // Optionally include the name of the department

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public UserRole getRole() {
        return role;
    }

    public void setRole(UserRole role) {
        this.role = role;
    }

    public Long getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Long departmentId) {
        this.departmentId = departmentId;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }
}